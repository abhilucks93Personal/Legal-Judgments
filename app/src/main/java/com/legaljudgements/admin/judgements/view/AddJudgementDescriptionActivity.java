    @Override
    public boolean onCreateOptionsMenu(Menu menu) {

        getMenuInflater().inflate(R.menu.judgement_desc, menu);

        return true;
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {

        switch (item.getItemId()) {
            case android.R.id.home:
                finish();
                return true;


            case R.id.desc_jm_done:
                submit();
                return true;

            default:
                return super.onOptionsItemSelected(item);
        }
    }

    private void submit() {
        strDesc = mEditor.getHtml();
        if (strDesc.length() > 0) {
            Constants.tempDetailedDescription = strDesc;
            Intent intent = new Intent();
            setResult(RESULT_OK, intent);
            finish();

        } else {
            Utility.showSnackBar(this, "No description is added.");
        }

    }


